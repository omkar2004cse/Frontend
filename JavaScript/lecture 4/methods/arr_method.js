let vegi_frut=["Tomato","Potato","Carroat"]

console.log("Array Methodes")
// Push method
vegi_frut.push("mango")
console.log("Push Method:-",vegi_frut)
// Pop method
console.log(vegi_frut.pop())
console.log(vegi_frut)

// tostring method is to convert arr into string 
// it is also applicable for the intergers
console.log(vegi_frut.toString())


//Concatination of array
console.log(vegi_frut.concat(['apple','onion']))

// unshift is used to add new element in the list
vegi_frut.unshift("Gava")
console.log(vegi_frut)

